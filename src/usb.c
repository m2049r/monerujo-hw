/* Buffer to be used for control requests. */
uint8_t usbd_control_buffer[128];

static int cdcacm_control_request(usbd_device *usbd_dev, struct usb_setup_data *req, uint8_t **buf,
		uint16_t *len, void (**complete)(usbd_device *usbd_dev, struct usb_setup_data *req))
{
	(void)complete;
	(void)buf;
	(void)usbd_dev;

	switch (req->bRequest) {
	case USB_CDC_REQ_SET_CONTROL_LINE_STATE: {
		/*
		 * This Linux cdc_acm driver requires this to be implemented
		 * even though it's optional in the CDC spec, and we don't
		 * advertise it in the ACM functional descriptor.
		 */
		char local_buf[10];
		struct usb_cdc_notification *notif = (void *)local_buf;

		/* We echo signals back to host as notification. */
		notif->bmRequestType = 0xA1;
		notif->bNotification = USB_CDC_NOTIFY_SERIAL_STATE;
		notif->wValue = 0;
		notif->wIndex = 0;
		notif->wLength = 2;
		local_buf[8] = req->wValue & 3;
		local_buf[9] = 0;
		// usbd_ep_write_packet(0x83, buf, 10);
		return 1;
		}
	case USB_CDC_REQ_SET_LINE_CODING:
		if (*len < sizeof(struct usb_cdc_line_coding))
			return 0;
		return 1;
	}
	return 0;
}

#define TX_BUFFER_SIZE 1024
static struct {
	char   buffer[TX_BUFFER_SIZE];
	size_t len;
	size_t offset;
} tx_buffer;

#define min(X,Y) ((X) < (Y) ? (X) : (Y))

static void cdcacm_data_tx_cb(usbd_device *usbd_dev, uint8_t ep)
{
	(void)ep;
	(void)usbd_dev;

	if (tx_buffer.len) {
		if (tx_buffer.offset >= tx_buffer.len) { // done sending - reset
			tx_buffer.len = 0;
			tx_buffer.offset = 0;
			return;
		}
		size_t len = min(tx_buffer.len-tx_buffer.offset, USB_MPS);
		usbd_ep_write_packet(usbd_dev, 0x82, tx_buffer.buffer+tx_buffer.offset, (uint16_t) len);
		tx_buffer.offset += len;
	}
}

static void cdcacm_data_rx_cb(usbd_device *usbd_dev, uint8_t ep)
{
	(void)ep;
	(void)usbd_dev;

	char buf[USB_MPS];
	uint16_t len = usbd_ep_read_packet(usbd_dev, 0x01, buf, USB_MPS);

	if (len) {
		//usbd_ep_write_packet(usbd_dev, 0x82, buf, len);
	}
}

static void cdcacm_set_config(usbd_device *usbd_dev, uint16_t wValue)
{
	(void)wValue;
	(void)usbd_dev;

	usbd_ep_setup(usbd_dev, 0x01, USB_ENDPOINT_ATTR_BULK, USB_MPS, cdcacm_data_rx_cb);
	usbd_ep_setup(usbd_dev, 0x82, USB_ENDPOINT_ATTR_BULK, USB_MPS, cdcacm_data_tx_cb);
	usbd_ep_setup(usbd_dev, 0x83, USB_ENDPOINT_ATTR_INTERRUPT, 16, NULL);

	usbd_register_control_callback(
				usbd_dev,
				USB_REQ_TYPE_CLASS | USB_REQ_TYPE_INTERFACE,
				USB_REQ_TYPE_TYPE | USB_REQ_TYPE_RECIPIENT,
				cdcacm_control_request);
}

static usbd_device *usbd_dev;

void usb_setup(void)
{
	// setup of ports was done in setup.c
	usbd_dev = usbd_init(&otgfs_usb_driver, &dev, &config, usb_strings,
			3, usbd_control_buffer, sizeof(usbd_control_buffer));
	usbd_register_set_config_callback(usbd_dev, cdcacm_set_config);
}

void usb_poll(void)
{
	// poll read buffer
	usbd_poll(usbd_dev);
}

// blocking write to usb
void usb_write(const char* msg)
{
	while (tx_buffer.len) usb_poll(); // wait for previous write to finish
	size_t len = strlen(msg);
	strncpy(tx_buffer.buffer, msg, TX_BUFFER_SIZE-1);
	tx_buffer.buffer[TX_BUFFER_SIZE] = 0;
	tx_buffer.len = len;
	tx_buffer.offset = 0;
	cdcacm_data_tx_cb(usbd_dev,0);
}
